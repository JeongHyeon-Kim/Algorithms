// problem source: https://leetcode.com/problems/subarray-sum-equals-k/
class Solution {
public:
    int subarraySum(vector<int>& nums, int k) {
        int nums_size = nums.size();
        for (int i = 0; i < nums_size; i++) {
            cout << nums[i] << " ";
        }
        cout << endl;
        cout << k << endl;
        return 0;
    }
};
