// problem source: https://leetcode.com/problems/minimum-remove-to-make-valid-parentheses/
class Solution {
public:
    string minRemoveToMakeValid(string s) {
        string result;
        int s_length = s.length();
        for (int i = 0; i < s_length; i++) {
            cout << s[i] << " ";
        }
        return result;
    }
};
