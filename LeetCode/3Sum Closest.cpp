// problem source: https://leetcode.com/problems/3sum-closest/
class Solution {
public:
    int threeSumClosest(vector<int>& nums, int target) {
        int nums_size = nums.size();
        for (int i = 0; i < nums_size; i++)
            cout << nums[i] << " ";
        cout << endl;
        cout << target << endl;
        return 0;
    }
};
