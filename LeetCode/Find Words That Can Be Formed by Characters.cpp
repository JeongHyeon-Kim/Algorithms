// problem source: https://leetcode.com/problems/find-words-that-can-be-formed-by-characters/
class Solution {
public:
    int countCharacters(vector<string>& words, string chars) {
        int words_size = words.size();
        for (int i = 0; i < words_size; i++)
            cout << words[i] << endl;
        cout << chars << endl;
        return 0;
    }
};
