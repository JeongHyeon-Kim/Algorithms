// problem source: https://leetcode.com/problems/longest-common-prefix/
class Solution {
public:
    string longestCommonPrefix(vector<string>& strs) {
        string result;
        int strs_size = strs.size();
        for (int i = 0; i < strs_size; i++)
            cout << strs[i] << endl;
        return result;
    }
};
