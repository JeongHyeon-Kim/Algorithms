// problem source: https://leetcode.com/problems/remove-duplicates-from-sorted-array/
class Solution {
public:
    int removeDuplicates(vector<int>& nums) {
        int nums_size = nums.size();
        for (int i = 0; i < nums_size; i++) {
            cout << nums[i] << endl;
        }
        return 0;
    }
};
